# include<stdio.h>

/*Average of Three numbers*/
int main(){
    int a,b,c;
    printf("Enter a : \n");
    scanf("%d",&a);
    printf("Enter b : \n");
    scanf("%d",&b);
    printf("Enter c : \n");
    scanf("%d",&c);
    
    printf("The Average value of these three numbers : %d",(a+b+c)/3);
    return 0;
}